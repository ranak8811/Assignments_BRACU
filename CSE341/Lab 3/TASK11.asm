ORG 100H

.DATA
A DB 'TRIANGLE IS VALID$'
B DB 'TRIANGEL IS NOT VALID$'

.CODE

MOV AH, 1
INT 21H
MOV BL, AL
SUB BL, 0

MOV AH, 1
INT 21H
MOV CL, AL
SUB BL, 0

MOV AH, 1
INT 21H
MOV CH, AL
SUB BL, 0

ADD BL, CL
CMP BL, CH
JL NP
JG MP

MP:
SUB BL, CL
ADD BL, CH
CMP BL, CL
JL NP
JG MMP

MMP:
SUB BL, CH
ADD CL, CH
CMP CL, BL
JL NP
JG AP

AP:
MOV AH, 9
LEA DX, A
INT 21H
JMP END

NP:
MOV AH, 9
LEA DX, B
INT 21H
JMP END

END:
RET
